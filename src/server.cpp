#if HAVE_CONFIG_H
# include <config.h>
#endif

#include <stddef.h>
#include <libtun6/ipv6-tunnel.h>

#include "server.h"

int MiredoServer::SendIPv6Packet (const void *packet,
						size_t length)
{
	return (tunnel != NULL) ?
		tunnel->SendPacket (packet, length)
		: -1;
}
